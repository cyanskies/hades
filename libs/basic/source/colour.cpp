namespace hades
{
	template<>
	colour from_string<colour>(std::string_view s)
	{
		for (auto i = std::size_t{}; i < size(colour_names); ++i)
		{
			if (s == colour_names[i])
				return colour_values[i];
		}

		throw bad_conversion{ "unrecognised colour" };
	}

	string to_string(colour c)
	{
		for (auto i = std::size_t{}; i < size(colour_names); ++i)
		{
			if (c == colour_values[i])
				return std::string{ colour_names[i] };
		}

		return "[r:" + to_string(c.r) +
			", g:" + to_string(c.g) +
			", b:" + to_string(c.b) +
			(c.a != std::numeric_limits<colour::value_type>::max() ? (", a:" + to_string(c.a)) : "") + 
			"]";
	}
}
